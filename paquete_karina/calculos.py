def sumar(n,m):
    """
    Esta función suma dos números.
    
    Parámetros:
    n (int): Primer número a sumar.
    m (int): Segundo número a sumar.
    
    Retorna:
    int: La suma de n y m.
    """
    return n + m

def multiplicar(n,m):
    """
    Esta función multiplica dos números.
    
    Parámetros:
    n (int): Primer número a multiplicar.
    m (int): Segundo número a multiplicar.
    
    Retorna:
    int: El producto de n y m.
    """
    return n * m
